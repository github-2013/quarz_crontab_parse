const parseCrontab = require("../dist/index.cjs");
describe('test hour', () => {
    test(' ? ? ? ? ? ? ? ', () => {
        expect(parseCrontab(' ? ? ? ? ? ? ? ')).toBe('')
    })

    test(' ? ? ? ? ? ? ?', () => {
        expect(parseCrontab(' ? ? ? ? ? ? ?')).toBe('')
    })

    test('? ? ? ? ? ? ? ', () => {
        expect(parseCrontab('? ? ? ? ? ? ? ')).toBe('')
    })

    test('? ? ? ? ? ? ?', () => {
        expect(parseCrontab('? ? ? ? ? ? ?')).toBe('')
    })

    test('? ? * ? ? ? ?', () => {
        expect(parseCrontab('? ? * ? ? ? ?')).toBe('')
    })

    test('? ? 0 ? ? ? ?', () => {
        expect(parseCrontab('? ? 0 ? ? ? ?')).toBe('')
    })

    test('? ? 15#3 ? ? ? ?', () => {
        expect(parseCrontab('? ? 15#3 ? ? ? ?')).toBe('15时的第3时')
    })

    test('? ? 5,10 ? ? ? ?', () => {
        expect(parseCrontab('? ? 5,10 ? ? ? ?')).toBe('第5和第10时')
    })

    test('? ? 5,10,15,20 ? ? ? ?', () => {
        expect(parseCrontab('? ? 5,10,15,20 ? ? ? ?')).toBe('第5、第10、第15和第20时')
    })

    test('? ? 10-40 ? ? ? ?', () => {
        expect(parseCrontab('? ? 10-40 ? ? ? ?')).toBe('10时到40时')
    })

    test('? ? 0/5 ? ? ? ?', () => {
        expect(parseCrontab('? ? 0/5 ? ? ? ?')).toBe('隔5时')
    })

    test('? ? */5 ? ? ? ?', () => {
        expect(parseCrontab('? ? */5 ? ? ? ?')).toBe('隔5时')
    })

    test('? ? 10/5 ? ? ? ?', () => {
        expect(parseCrontab('? ? 10/5 ? ? ? ?')).toBe('从第10时开始，每隔5时')
    })
})
