const parseCrontab = require("../dist/index.cjs");
describe('test month', () => {
    test('? ? ? ? ? ? ?', () => {
        expect(parseCrontab('? ? ? ? ? ? ?')).toBe('')
    })

    test('? ? ? ? * ? ?', () => {
        expect(parseCrontab('? ? ? ? * ? ?')).toBe('')
    })

    test('? ? ? ? 0 ? ?', () => {
        expect(parseCrontab('? ? ? ? 0 ? ?')).toBe('')
    })

    test('? ? ? ? 10#3 ? ?', () => {
        expect(parseCrontab('? ? ? ? 10#3 ? ?')).toBe('第3个10月')
    })

    test('? ? ? ? 5,10 ? ?', () => {
        expect(parseCrontab('? ? ? ? 5,10 ? ?')).toBe('第5和第10月')
    })

    test('? ? ? ? 5,10,15,20 ? ?', () => {
        expect(parseCrontab('? ? ? ? 5,10,15,20 ? ?')).toBe('第5、第10、第15和第20月')
    })

    test('? ? ? ? 10-40 ? ?', () => {
        expect(parseCrontab('? ? ? ? 10-40 ? ?')).toBe('10月到40月')
    })

    test('? ? ? ? 0/5 ? ?', () => {
        expect(parseCrontab('? ? ? ? 0/5 ? ?')).toBe('隔5月')
    })

    test('? ? ? ? */5 ? ?', () => {
        expect(parseCrontab('? ? ? ? */5 ? ?')).toBe('隔5月')
    })

    test('? ? ? ? 10/5 ? ?', () => {
        expect(parseCrontab('? ? ? ? 10/5 ? ?')).toBe('从第10月开始，每隔5月')
    })

    test('? ? ? ? 5L ? ?', () => {
        expect(parseCrontab('? ? ? ? 5L ? ?')).toBe('最后一月的星期四')
    })
})
