const parseCrontab = require("../dist/index.cjs");
describe('test second', () => {
    test(' ? ? ? ? ? ? ? ', () => {
        expect(parseCrontab(' ? ? ? ? ? ? ? ')).toBe('')
    })

    test(' ? ? ? ? ? ? ?', () => {
        expect(parseCrontab(' ? ? ? ? ? ? ?')).toBe('')
    })

    test('? ? ? ? ? ? ? ', () => {
        expect(parseCrontab('? ? ? ? ? ? ? ')).toBe('')
    })

    test('? ? ? ? ? ? ?', () => {
        expect(parseCrontab('? ? ? ? ? ? ?')).toBe('')
    })

    test('* ? ? ? ? ? ?', () => {
        expect(parseCrontab('* ? ? ? ? ? ?')).toBe('每秒')
    })

    test('0 ? ? ? ? ? ?', () => {
        expect(parseCrontab('0 ? ? ? ? ? ?')).toBe('0秒')
    })

    test('15#3 ? ? ? ? ? ?', () => {
        expect(parseCrontab('15#3 ? ? ? ? ? ?')).toBe('第3个15秒')
    })

    test('5,10 ? ? ? ? ? ?', () => {
        expect(parseCrontab('5,10 ? ? ? ? ? ?')).toBe('第5和第10秒')
    })

    test('5,10,15,20 ? ? ? ? ? ?', () => {
        expect(parseCrontab('5,10,15,20 ? ? ? ? ? ?')).toBe('第5、第10、第15和第20秒')
    })

    test('10-40 ? ? ? ? ? ?', () => {
        expect(parseCrontab('10-40 ? ? ? ? ? ?')).toBe('10秒到40秒')
    })

    test('0/5 ? ? ? ? ? ?', () => {
        expect(parseCrontab('0/5 ? ? ? ? ? ?')).toBe('从0秒开始到55秒范围内每隔5秒')
    })

    test('*/5 ? ? ? ? ? ?', () => {
        expect(parseCrontab('*/5 ? ? ? ? ? ?')).toBe('从0秒开始到55秒范围内每隔5秒')
    })

    test('10/5 ? ? ? ? ? ?', () => {
        expect(parseCrontab('10/5 ? ? ? ? ? ?')).toBe('从10秒开始到65秒范围内每隔5秒')
    })
})
