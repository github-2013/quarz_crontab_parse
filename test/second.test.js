const parseCrontab = require("../dist/index.cjs");
describe('test second', () => {
    test(' ? ? ? ? ? ? ? ', () => {
        expect(parseCrontab(' ? ? ? ? ? ? ? ')).toBe('')
    })

    test(' ? ? ? ? ? ? ?', () => {
        expect(parseCrontab(' ? ? ? ? ? ? ?')).toBe('')
    })

    test('? ? ? ? ? ? ? ', () => {
        expect(parseCrontab('? ? ? ? ? ? ? ')).toBe('')
    })

    test('? ? ? ? ? ? ?', () => {
        expect(parseCrontab('? ? ? ? ? ? ?')).toBe('')
    })

    test('* ? ? ? ? ? ?', () => {
        expect(parseCrontab('* ? ? ? ? ? ?')).toBe('')
    })

    test('0 ? ? ? ? ? ?', () => {
        expect(parseCrontab('0 ? ? ? ? ? ?')).toBe('')
    })

    test('15#3 ? ? ? ? ? ?', () => {
        expect(parseCrontab('15#3 ? ? ? ? ? ?')).toBe('15秒的第3秒')
    })

    test('5,10 ? ? ? ? ? ?', () => {
        expect(parseCrontab('5,10 ? ? ? ? ? ?')).toBe('第5和第10秒')
    })

    test('5,10,15,20 ? ? ? ? ? ?', () => {
        expect(parseCrontab('5,10,15,20 ? ? ? ? ? ?')).toBe('第5、第10、第15和第20秒')
    })

    test('10-40 ? ? ? ? ? ?', () => {
        expect(parseCrontab('10-40 ? ? ? ? ? ?')).toBe('10秒到40秒')
    })

    test('0/5 ? ? ? ? ? ?', () => {
        expect(parseCrontab('0/5 ? ? ? ? ? ?')).toBe('隔5秒')
    })

    test('*/5 ? ? ? ? ? ?', () => {
        expect(parseCrontab('*/5 ? ? ? ? ? ?')).toBe('隔5秒')
    })

    test('10/5 ? ? ? ? ? ?', () => {
        expect(parseCrontab('10/5 ? ? ? ? ? ?')).toBe('从第10秒开始，每隔5秒')
    })
})
